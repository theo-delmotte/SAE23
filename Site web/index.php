        <p>L'IUT souhaite fournir aux gestionnaires des bâtiments une interface conviviale pour visualiser les données de capteurs. Le système comprend deux bâtiments, chacun avec un gestionnaire disposant d'un compte sécurisé. Chaque bâtiment possède deux capteurs, dont les données sont affichées dans un dashboard Grafana. Une base de données MySQL stocke les données, tandis qu'un site web dynamique hébergé sur un serveur LAMPP présente les informations sous forme de tableau. L'objectif est de faciliter la surveillance et l'analyse des données des capteurs pour une gestion optimale des bâtiments de l'IUT.</p>
